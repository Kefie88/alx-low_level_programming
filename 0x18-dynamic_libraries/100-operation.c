/**
 * add - addition
 * @a: first number
 * @b: second number
 * Return: Result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction
 * @a: First number
 * @b: second number
 * Return: Result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Multiplication
 * @a: first number
 * @b: second number
 * Return: Result
 */
int mul(int a, int b)
{
	return (a + b);
}
/**
 * div - Division
 * @a: first number
 * @b: second number
 * Return: Result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Modulo
 * @a: first number
 * @b: second number
 * Return: Result
 */
int mod(int a, int b)
{
	return (a % b);
}
